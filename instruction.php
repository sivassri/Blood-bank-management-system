<h1>Genral Guidelines for Blood Donation</h1>
<p>There are several parameters that determine the eligibility of an individual to donate blood. The following guidelines are laid down by the Ministry of Health, Government of India.


<br><ul><li>Overall health- The donor must be fit and healthy, and should not be suffering from transmittable diseases.</li>
<br><li>Age and weight- The donor must be 18–65 years old and should weigh a minimum of 50  kg.</li>
<br><li>Pulse rate- Between 50 and 100 without irregularities.</li>
<br><li>Hemoglobin level- A minimum of 12.5 g/dL.</li>
<br><li>Body temperature- Should be normal, with an oral temperature not exceeding 37.5 °C.</li>
<br><li>The time period between successive blood donations should be more than 3 months.</li></ul>
</p>
<p>
<br><br>Individuals under certain conditions are deemed ineligible to donate blood:

<br><ol><li>A person who has been tested HIV positive.</li>
<br><li>ndividuals suffering from ailments like cardiac arrest, hypertension, blood pressure, cancer, epilepsy, kidney ailments and diabetes.</li>
<br><li>A person who has undergone ear/body piercing or tattoo in the past 6 months.</li>
<br><li>Individuals who have undergone immunization in the past 1 month.</li>
<br><li>Individuals treated for rabies or received Hepatitis B vaccine in the past 6 months.</li>
<br><li>A person who has consumed alcohol in the past 24 hours.</li>
<br><li>Women who are pregnant or breastfeeding.</li>
<br><li>Individuals who have undergone major dental procedures or general surgeries in the past 1 month.</li>
<br><li>Women who have had miscarriage in the past 6 months.</li>
<br><li>Individuals who have had fits, tuberculosis, asthma and allergic disorders in the past.</li></ol>
</p>

